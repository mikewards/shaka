/// Maximum RGB Euclidean distance to accept a match.
/// GIBS tile PNG compression can shift values by a few units.
const _maxDistance = 30.0;

/// Estimate chlorophyll-a (mg/m3) from a GIBS satellite hex color string.
///
/// Returns null if the color is not recognizable as a GIBS chlorophyll color
/// (e.g. land, cloud artifact, or non-chlorophyll layer).
double? estimateChlorophyllFromHex(String hexColor) {
  final rgb = _parseHex(hexColor);
  if (rgb == null) return null;
  final (r, g, b) = rgb;

  double bestDist = double.infinity;
  double bestChl = 0;

  for (final (cr, cg, cb, chl) in _gibsColormap) {
    final dr = (r - cr).toDouble();
    final dg = (g - cg).toDouble();
    final db = (b - cb).toDouble();
    final dist = sqrt(dr * dr + dg * dg + db * db);
    if (dist < bestDist) {
      bestDist = dist;
      bestChl = chl;
      if (dist == 0) break;
    }
  }

  if (bestDist > _maxDistance) return null;
  return bestChl;
}

(int, int, int)? _parseHex(String hex) {
  try {
    final h = hex.replaceFirst('#', '');
    if (h.length != 6) return null;
    final v = int.parse(h, radix: 16);
    return ((v >> 16) & 0xFF, (v >> 8) & 0xFF, v & 0xFF);
  } catch (_) {
    return null;
  }
}
